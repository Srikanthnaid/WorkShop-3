package WorkShop3;

public class Hotel {
	// Class Based variables
	String hotelName;
	int rating;

	//  contractor
	public Hotel() {

	}

	// creating getter and setter methods.
	public String getHotelName() {
		return hotelName;
	}

	public void setHotelName(String hotelName) {
		this.hotelName = hotelName;
	}

	public int getRating() {
		return rating;
	}

	public void setRating(int rating) {
		this.rating = rating;
	}

}